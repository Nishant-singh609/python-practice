# operator in python
# 1. Arithmetic Operators (+, -, *, /, %)
a = 10
b = 3

print(a + b)   # 13
print(a - b)   # 7
print(a * b)   # 30
print(a / b)   # 3.33
print(a % b)   # 1 (remainder)

# 2. Comparison Operators (==, !=, >, <, >=, <=)
a = 10
b = 5

print(a == b)   # False
print(a != b)   # True
print(a > b)    # True
print(a < b)    # False
# 3. Logical Operators (and, or, not)
x = True
y = False

print(x and y)  # False
print(x or y)   # True
print(not x)    # False
# 4. Assignment Operators (=, +=, -=, *=)
a = 5
a += 3   # a = a + 3
print(a) # 8
# 5. Membership Operators (in, not in)
name = "Nishant"

print("N" in name)      # True
print("z" not in name)  # True
# 6. Identity Operators (is, is not)
a = 5
b = 5

print(a is b)      # True
print(a is not b)  # False